    public void drawSeats(Graphics g) {
        Hall hal = new Hall("Sal 1", 10, 10);
        hal.fillSeats();
        for (Seat[] chair: hal.seats) {
            for (Seat seat: chair) {
                seat.setX(getX()+10);
                seat.setY(getY()+10);
                seat.draw(g);
            }
        }
        repaint();
    }
